//单例
+(id)sharedInstance
{
    //此种单例创建优点
    //1. 线程安全。
    //2. 满足静态分析器的要求。
    //3. 兼容了ARC
    static RSACodeTool *tool = nil;
    static dispatch_once_t onceToken;
    dispatch_once(&onceToken, ^{
        tool = [[self alloc]init];
    });
    return tool;
}

- (void)dealloc {
    if (nil != _publicKey) {
        CFRelease(_publicKey);
    }
    
    if (nil != _privateKey) {
        CFRelease(_privateKey);
    }
}

